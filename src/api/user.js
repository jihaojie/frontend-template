export function login(data) {
  return request({
    url: base + '/auth',
    method: 'post',
    data
  })
}

export function logout(data) {
  return request({
    url: base + '/logout',
    method: 'post',
    data
  })
}

export function getInfo(token) {
  return request({
    url: '/vue-admin-template/user/info',
    method: 'get',
    params: {token}
  })
}

